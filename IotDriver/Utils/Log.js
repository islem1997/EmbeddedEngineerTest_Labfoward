/*
 * Logging wrapper
 */
const log = {};

log.info = function info(message) {
  console.log('\x1b[34m' ,message,'\x1b[0m');
};

log.error = function error(message) {
    console.log("\x1b[31m" ,message,'\x1b[0m');
};

log.success = function debug(message) {
    console.log('\x1b[32m' ,message,'\x1b[0m');
};

module.exports = log;